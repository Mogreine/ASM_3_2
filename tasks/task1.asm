.MODEL SMALL
.STACK 200h
.386

.DATA
	NUMBER 	DB	"A06$"
	RES DB 32 DUP("$")
.CODE
EXTRN TO_LOWER: FAR
EXTRN HEX_TO_INT32: FAR
EXTRN TO_BINIRY_STRING: FAR

START:
	MOV AX, @DATA
	MOV DS, AX
	
	MOV DX, OFFSET NUMBER
	CALL HEX_TO_INT32
	MOV ECX, EDX
	MOV DX, OFFSET RES
	CALL TO_BINIRY_STRING
	
	MOV AH, 9
	INT 21H
	
	MOV AH, 4CH
	INT 21H	
END START